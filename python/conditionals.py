temp = int(input("What is the temperature outside? "))

if temp > 80: 
    print("It's too hot!")
    print("Stay inside.")
elif temp < 60:
    print("Too cold. Stay inside.")

else: 
    print("Enjoy the outdoors!")

if not temp == 75:
    print("It's not the perfect temp")
else: 
    print("This is the perfect temp!")